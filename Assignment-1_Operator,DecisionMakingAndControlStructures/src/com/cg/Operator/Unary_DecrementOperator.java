package com.cg.Operator;

public class Unary_DecrementOperator {

	public static void main(String[] args) {
		//PreDecrement= --a
	   //PostDecrement= a--
		       int a=6,b=8;
		       int X=a--;
		       int Y=--b;
		       int Z=a+X;
		       System.out.println(Y);
		       System.out.println(Z);
	}

}
